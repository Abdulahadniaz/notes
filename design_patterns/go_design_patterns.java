/*
 * Based on book
 * "Design Patterns: Elements of Reusable Object-Oriented Software"
 * As there are 4 authors, these patterns are also called "Gang of Four" design
 * patterns.
 * 
 * There are 23 design patterns in total.
 * 
 * And they are divided into 3 categories:
 * 1- Creational Patterns
 * 2- Structural Patterns
 * 3- Behavioral Patterns
 * 
 * 1 - Creational Patterns
 * The design patterns that deal with object creation mechanisms,
 * trying to create objects in a way suitable to the situation.
 * 
 * 2 - Structural Patterns
 * The design patterns in this category deals with the class structure such as
 * Inheritance and Composition
 * 
 * 3 - Behavioral Patterns
 * This type of design patterns provide solution for the better interaction
 * between objects, how to provide lose coupling, and flexibility to extend
 * easily in future.
 */